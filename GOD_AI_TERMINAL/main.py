def main():
    print_logo()
    help_menu()

    while True:
        cmd = input(Fore.YELLOW + "\nūüíĽ > ").strip().lower()

        if cmd == "help":
            help_menu()
        elif cmd == "tasks":
            tasks.task_menu()
        elif cmd == "vault":
            vault.vault_menu()
        elif cmd == "games":
            games.launch_games()
        elif cmd == "files":
            file_tools.run_tools()
